#!/usr/bin/env bash
#
# run_cluster_jsd.sh -- run the pipeline with the bounded Jensen-Shannon score
# (--score-method jsd) into a separate results folder, as a third robustness
# axis alongside the default-vs-qnorm normalization comparison.
#
# Derives its config from the live base config, overriding ONLY outdir + the
# score_method knob (normalization stays whatever the base config uses; by
# default that is OFF, matching the production default run). The observed scores
# AND the permutation null are both computed with JSD, so the null matches.
#
#   bash workflow/run_cluster_jsd.sh            # core path -> results_jsd/
#   bash workflow/run_cluster_jsd.sh --full     # full pipeline
#   bash workflow/run_cluster_jsd.sh --dry      # dry run only
#
# Compare against the default (KL) run with compare_default_vs_qnorm.py:
#   python compare_default_vs_qnorm.py \
#     --default-dir workflow/results/pvalue \
#     --qnorm-dir   workflow/results_jsd/pvalue
# (here 'qnorm-dir' is the JSD run; the Jaccard/rho it reports is the
#  KL-vs-JSD scoring-method concordance, not a normalization effect.)
# ASCII-only.

set -euo pipefail

REPO="$(cd "$(dirname "$0")/.." && pwd)"
BASE_CONFIG="$REPO/workflow/config/config.yaml"
PROFILE="$REPO/workflow/profiles/slurm"

FULL=0
DRY=0
while [ $# -gt 0 ]; do
  case "$1" in
    --full) FULL=1; shift ;;
    --dry)  DRY=1; shift ;;
    *) echo "unknown arg: $1" >&2; exit 2 ;;
  esac
done

DERIVED_CONFIG="$REPO/workflow/config/config_jsd.generated.yaml"
python3 - "$BASE_CONFIG" "$DERIVED_CONFIG" << 'PY'
import sys, yaml
base_path, out_path = sys.argv[1:3]
with open(base_path) as fh:
    cfg = yaml.safe_load(fh)
base_out = cfg.get("outdir", "results")
cfg["outdir"] = "%s_jsd" % base_out
cfg["score_method"] = "jsd"
header = ("# AUTO-GENERATED by run_cluster_jsd.sh from %s.\n"
          "# Differs from the base config ONLY in: outdir, score_method=jsd.\n" % base_path)
with open(out_path, "w") as fh:
    fh.write(header); yaml.safe_dump(cfg, fh, default_flow_style=False, sort_keys=False)
print("  base outdir  : %s" % base_out)
print("  jsd outdir   : %s" % cfg["outdir"])
print("  score_method : jsd")
PY

CONFIG="$DERIVED_CONFIG"
OUT=$(python3 -c "import yaml;print(yaml.safe_load(open('$CONFIG'))['outdir'])")
echo "== JSD run -> $OUT =="; echo

echo "== Preflight =="
python3 "$REPO/workflow/preflight.py" --configfile "$CONFIG" --core-only
echo

SM_TARGETS=("$REPO/workflow/$OUT/pvalue.done")
[ "$FULL" = "1" ] && SM_TARGETS=()

echo "== Dry run =="
snakemake -s "$REPO/workflow/Snakefile" --configfile "$CONFIG" -n "${SM_TARGETS[@]}"
if [ "$DRY" = "1" ]; then echo; echo "Dry run only."; exit 0; fi

echo; echo "== Submitting to SLURM =="
snakemake -s "$REPO/workflow/Snakefile" --configfile "$CONFIG" \
  --profile "$PROFILE" "${SM_TARGETS[@]}"

echo; echo "Done -> workflow/$OUT/. Compare against the default (KL) run:"
echo "  python compare_default_vs_qnorm.py \\"
echo "    --default-dir workflow/results/pvalue \\"
echo "    --qnorm-dir   workflow/$OUT/pvalue"
